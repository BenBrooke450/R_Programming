
# Absolute value
abs(-10)            # Returns 10
#[1] 10

# Square root
sqrt(16)            # Returns 4
#[1] 4

# Rounding numbers
round(3.14159)      # Returns 3
#[1] 3

round(3.14159, 2)   # Returns 3.14
#[1] 3.14

# Get help on a function
?round              # Shows help documentation
args(round)         # Shows arguments the function takes




